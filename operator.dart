void main() {
  var m = 25;
  var n = 4;
  print("m+n - ${m + n}");
  print("++m - ${++m}");
  print("m++ - ${m++}");
  print("--m - ${--m}");
  print("m-- - ${m--}");
  print("m-n - ${m - n}");
  print("m/n - ${m / n}");
  print("m*n - ${m * n}");
  print("m%n - ${m % n}");
  print("m~/n - ${m ~/ n}");
}
